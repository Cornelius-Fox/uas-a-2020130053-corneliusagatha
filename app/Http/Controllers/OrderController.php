    {
        $rules = [
            'status' => 'required|max:255',

        ];

        $validated = $request->validate($rules);

        Order::create($validated);

        if($request->has('menu'))
        {
            $orders->menu()->attach($request->orders);
        }

        $request->session()->flash('succes', "Berhasil Menyimpan Menu ke Database yang bernama{$validated['status']}");
        return redirect()->route('orders.index');


    }

